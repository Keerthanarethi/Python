#if-else statements [if,if-else,if-else-elif,nested if-else-elif]
num = int(input("Enter the value of num:")) 
if (num < 0):
    print("number is negative.")
elif (num==0):
    print("number is zero.") 
elif (num==999):
    print("number is special.")   
else:
    print("number is positive.")     
    